    private void PhonenoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PhonenoKeyTyped
        String p=Phoneno.getText();
        int length=p.length();
        char c=evt.getKeyChar();
        if(c>='0' && c<='9'|| c==(char)KeyEvent.VK_BACK_SPACE ||c==(char)KeyEvent.VK_DELETE  )
        {
            if(length<10 || length==10 && c==(char)KeyEvent.VK_BACK_SPACE ||c==(char)KeyEvent.VK_DELETE)
            {
                Phoneno.setEditable(true);
            }
            else
                Phoneno.setEditable(false);
        }
        else
        {
            if(evt.getKeyCode()==KeyEvent.VK_BACK_SPACE ||evt.getKeyCode()==KeyEvent.VK_BACK_SPACE  )
            {
                Phoneno.setEditable(true);
            }
            else
            {
                Phoneno.setEditable(false);
            }
        }
    }//GEN-LAST:event_PhonenoKeyTyped

