    private void SignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpActionPerformed
        // TODO add your handling code here:
        String gname,gphoneno,gemail,gpassword;
        gname=Name.getText();
        gphoneno=Phoneno.getText();
        gemail=Emailid.getText();
        gpassword=Password.getText();
        
        if((("".equals(gname) || "".equals(gphoneno)) || "".equals(gemail))||"".equals(gpassword))
        {
            Name.setText("Enter data");
            Phoneno.setText("Enter data");
            Emailid.setText("Enter data");
            Password.setText("");
            
        }
        else
        {
            try
            {
                
                conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/CanteenSystem","root","root");
                st=conn.prepareStatement("insert into login(name,phoneno,emailid,password)values(?,?,?,?)");
                st.setString(1, gname);
                st.setString(2, gphoneno);
                st.setString(3, gemail);
                st.setString(4, gpassword);
                
                
                st.executeUpdate();
                SignIn signin=new SignIn();
                signin.setVisible(true);
                this.setVisible(false);
                
                
                
            }
            catch (SQLException ex)
            {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
    }//GEN-LAST:event_SignUpActionPerformed

